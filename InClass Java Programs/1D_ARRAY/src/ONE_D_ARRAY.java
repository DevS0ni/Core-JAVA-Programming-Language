
public class ONE_D_ARRAY 
{
    public static void main(String[] args) 
    {
        int no[]=new int[5];
        no[0]=10;
        no[1]=20;
        no[2]=30;
        no[3]=40;
        no[4]=50;
        
        System.out.println(no[0]);
        System.out.println(no[1]);
        System.out.println(no[2]);
        System.out.println(no[3]);
        System.out.println(no[4]);
        
    }
    
}
