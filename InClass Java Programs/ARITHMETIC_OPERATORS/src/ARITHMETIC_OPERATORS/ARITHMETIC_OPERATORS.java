
package ARITHMETIC_OPERATORS;

public class ARITHMETIC_OPERATORS 
{
    public static void main(String args[])
    {
        int no1=10,no2=10;
        
        System.out.print("ADDITION : "+(no1+no2)+"\n");
        System.out.print("SUBTRACTION : "+(no1-no2)+"\n");
        System.out.print("MULTIPLICATION : "+(no1*no2)+"\n");
        System.out.print("DIVISION : "+(no1/no2)+"\n");
        System.out.print("MODULUS : "+(no1%no2)+"\n");
        
    }
}
