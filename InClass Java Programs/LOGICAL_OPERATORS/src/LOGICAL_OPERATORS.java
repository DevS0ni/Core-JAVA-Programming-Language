
public class LOGICAL_OPERATORS {

    public static void main(String[] args) 
    {
        System.out.println((15>8) && (5>10));
        System.out.println((10>2) && (10>5));
        
        System.out.println((10>2) || (10<5));
        System.out.println(('A'=='a') || ('B'=='b'));
        
        System.out.println(!(5==2));
        System.out.println(!(5==5));
    }
    
}
